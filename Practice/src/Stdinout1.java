import java.util.*;
public class Stdinout1 {
			public static void main(String[] args) {
		        Scanner scan = new Scanner(System.in);
		        int a = scan.nextInt();
		        System.out.println(a);
		        a = scan.nextInt();
		        System.out.println(a);
		        a = scan.nextInt();
		        System.out.print(a);
		        
		        scan.close();
	    }
}
